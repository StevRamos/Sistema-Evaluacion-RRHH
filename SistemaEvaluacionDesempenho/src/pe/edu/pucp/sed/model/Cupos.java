public class Cupos{
	private int numeroCupos;
	private Area area;
	private EscalaPeriodo escala;

	public int getNumeroCupos(){
		return this.numeroCupos;
	};

	public void setNumeroCupos( int numeroCupos ){
		this.numeroCupos = numeroCupos;
	};

	public Area getArea(){
		return this.area;
	};

	public void setArea( Area area ){
		this.area = area;
	};

	public EscalaPeriodo getEscala(){
		return this.escala;
	};

	public void setEscala( EscalaPeriodo escala ){
		this.escala = escala;
	};

}

